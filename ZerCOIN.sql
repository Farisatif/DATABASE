CREATE DATABASE ZerCoin;

USE ZerCoin;

CREATE TABLE ZarCoin (

Zer_ID INT PRIMARY KEY IDENTITY (1,1),
Zer_NAME NVARCHAR (30),
Zer_Gmail NVARCHAR (100)

);


CREATE TABLE ZarVIP(
Zer_ID INT PRIMARY KEY IDENTITY (1,1),
Zer_COIN NVARCHAR (30) NOT NULL,
Zer_NUM INT NOT NULL 
);

CREATE TABLE ZarCOIN(
C_ID INT NOT NULL,
C_NAME NVARCHAR (10)

);

CREATE TABLE ZarC(

C_ID INT CHECK (C_ID > 0),
C_NUM INT CHECK (C_NUM > 0),
C_CODE INT UNIQUE
)
